class CustomFontSize {
  /// プライベートコンストラクタで外部からのインスタンス化を防止
  CustomFontSize._();

  static const double smallest = 10; //メッセージルームの時間
  static const double mini = 12; //カードの中の小さい説明など
  static const double small = 14; //小さい文字。（小見出しの下の文字など）
  static const double medium = 16; //テキストフォームフィールドやボタンの中の文字、小見出し
  static const double normal = 18; //通常
  static const double large = 22; //少し目立たせたい時
  static const double largest = 26; //とくに目立たせたい時
}
